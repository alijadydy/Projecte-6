# CONSTANT
PI = 3.1416

# FUNCIÓ
def calcular_perimetre(radi):
    return 2 * PI * radi

# ENTRADA DE L'USUARI
radi = float(input("Introdueix el radi: "))

# SORTIDA PER PANTALLA
print("El perímetre del cercle és:", calcular_perimetre(radi))
